package StarPatter;

import java.util.Iterator;

public class Star {

	public static void main(String[] args) 
	{
		
		int line=4;
		int starCount=4;
		for (int i = 0; i <=line; i++) 
		{
		for (int j = 0; j <=starCount; j++)
		{
			System.out.print("*");	
		}	
		System.out.println();
		}
		
	}

}
