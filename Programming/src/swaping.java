
public class swaping
{

	public static void main(String[] args)
	{
	
		int a=20;
		int b=30;
		System.out.println("before swapping");
		System.out.println("value of A is"+" "+a +" B is"+" "+" "+b);
			 int temp=0;
		temp=a;
		a=b;
		b=temp;
		System.out.println("After swapping");
		System.out.println("value of A is"+" "+a+" "+ "B is "+" "+b);
		
	}
}
