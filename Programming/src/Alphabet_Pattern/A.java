package Alphabet_Pattern;

public class A {

	public static void main(String[] args) 
	
	{
	
		int line=5;
		int CharCount=5;
		char ch='A';
		
		for (int i = 0; i <=line; i++)
		{
		
			for (int j = 0; j <=CharCount; j++) 
			{
			
				System.out.print(ch); 
			}
			System.out.println();
		}

	}

}
