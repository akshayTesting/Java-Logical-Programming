package UnaryOprators;

public class RationalOperator {

	public static void main(String[] args)
	{

		
		int a=10;
		int b=15;
		int c=10;

	System.out.println("Value of A is "+a);
	System.out.println("Value of B is "+b);
	System.out.println("Value of C is "+c);
	System.out.println("================================");
	System.out.println("A==b "+(a==b));
	System.out.println("A==C "+(a==c));
	System.out.println("B==C "+(b==c));
	
	System.out.println("================================");
	System.out.println("A is not Equal to B "+(a!=b));
	System.out.println("A is not Equal to C "+(a!=c));
	System.out.println("C is not Equal to A "+(c!=a));
	
	System.out.println("================================");

	System.out.println("A is Greater Then  B "+(a<b));
	System.out.println("A is Less Then  B "+(a>b));
	System.out.println("A is Greater Then  C "+(a<c));
	System.out.println("================================");
	System.out.println("A is LessThen Or Equal TO B "+(a<=b));
	System.out.println("C is Greater Then OR Equal TO A "+(c>=a));
	System.out.println("B is LessThen Or Equal To C "+(b>=c));
	System.out.println("====================================");
	
	}

}
